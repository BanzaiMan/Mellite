package de.sciss.mellite
package gui
package impl
package tracktool

import de.sciss.synth.proc.Sys
import java.awt.event.MouseEvent

object BasicRegion {
  final val MinDur  = 32
}
trait BasicRegion[S <: Sys[S], A] extends RegionImpl[S, A] with Dragging[S, A] {
  import TrackTool._

  protected type Initial = timeline.ProcView[S]

  final protected def handleSelect(e: MouseEvent, hitTrack: Int, pos: Long, region: timeline.ProcView[S]): Unit =
    if (e.getClickCount == 2) {
      handleDoubleClick()
    } else {
      new Drag(e, hitTrack, pos, region)
    }

  protected def dialog(): Option[A]

  final protected def handleDoubleClick(): Unit =
    dialog().foreach { p =>
      dispatch(DragBegin)
      dispatch(DragAdjust(p))
      dispatch(DragEnd)
    }

